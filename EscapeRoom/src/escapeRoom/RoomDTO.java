package escapeRoom;

public class RoomDTO {

	private String id, password, memberRank;
	private int memberNum, money;
	
	public RoomDTO() {
		
	}

	public RoomDTO(String id, String password, String memberRank, int memberNum, int money) {
		super();
		this.id = id;
		this.password = password;
		this.memberRank = memberRank;
		this.memberNum = memberNum;
		this.money = money;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this. id = id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getMemberRank() {
		return memberRank;
	}

	public void setMemberRank(String memberRank) {
		this.memberRank = memberRank;
	}

	public int getMemberNum() {
		return memberNum;
	}

	public void setMemberNum(int memberNum) {
		this.memberNum = memberNum;
	}

	public int getMoney() {
		return money;
	}

	public void setMoney(int money) {
		this.money = money;
	}

	@Override
	public String toString() {
		return "RoomDTO [Id=" + id + ", password=" + password + ", memberRank=" + memberRank + ", memberNum="
				+ memberNum + ", money=" + money + "]";
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
